#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*s2;

	i = 0;
	s2 = (char *)s;
	if (!s2)
		return (NULL);
	else
	{
		while (s2[i] != c)
		{
			if (s2[i] == '\0')
				return (NULL);
			i++;
		}
		return (&s2[i]);
	}
}
